package commom;

public class Feedback_User_Bean {
	
	
	private int id;
	private String feedfirstname;
	private String feedlastname;
	private String feedemail;
	private String feedbacks;
	
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFeedfirstname() {
		return feedfirstname;
	}
	public void setFeedfirstname(String feedfirstname) {
		this.feedfirstname = feedfirstname;
	}
	public String getFeedlastname() {
		return feedlastname;
	}
	public void setFeedlastname(String feedlastname) {
		this.feedlastname = feedlastname;
	}
	public String getFeedemail() {
		return feedemail;
	}
	public void setFeedemail(String feedemail) {
		this.feedemail = feedemail;
	}
	public String getFeedbacks() {
		return feedbacks;
	}
	public void setFeedbacks(String feedbacks) {
		this.feedbacks = feedbacks;
	}
	
	
	
	
	

}
