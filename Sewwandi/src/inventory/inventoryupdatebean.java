package inventory;

public class inventoryupdatebean {

	
	private int itemid;
	private String itemname;
	private int itemamount;
	private int itemaddedamount;
	private String itemupdatedate;
	
	public int getItemid() {
		return itemid;
	}
	public void setItemid(int itemid) {
		this.itemid = itemid;
	}
	public String getItemname() {
		return itemname;
	}
	public void setItemname(String itemname) {
		this.itemname = itemname;
	}
	public int getItemamount() {
		return itemamount;
	}
	public void setItemamount(int itemamount) {
		this.itemamount = itemamount;
	}
	public int getItemaddedamount() {
		return itemaddedamount;
	}
	public void setItemaddedamount(int itemaddedamount) {
		this.itemaddedamount = itemaddedamount;
	}
	public String getItemupdatedate() {
		return itemupdatedate;
	}
	public void setItemupdatedate(String itemupdatedate) {
		this.itemupdatedate = itemupdatedate;
	}
	
	
	
}

